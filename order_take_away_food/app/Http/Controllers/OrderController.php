     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show()
    {
        $user_id = Auth::user()->id;


        if ($user_id == 1) {
            $orders = Order::all();
            return view('showAdminOrder',[
                'orders' => $orders
            ]);
        }

        else {
            $orders = Order::where('user_id',$user_id)->get();
            return view('showUserOrder',[
                'orders' => $orders
            ]);
        }
    }

